    void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        CourseDAO dao = new CourseDAO();
        List<Course> list = dao.getAllCourses();
        
        if(list != null) {
            for (Course s : list) {
                Object[] row = {
                    s.getCourseid(),
                    s.getCoursename(),
                    s.getCoursedes(),
                    s.getCredits()
                };
                model.addRow(row);
            }
        }
    }
    
