    private void addCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseBtnActionPerformed

        String courseName = courseNameField.getText();
        String courseDes = courseDesField.getText();
        String credStr = creditsField.getText();
        
        int cred;

        if (courseName.isEmpty() || courseDes.isEmpty() || credStr.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!");
            return;
        }

        try {
            cred = Integer.parseInt(credStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Credits must be a valid number!");
            return;
        }

        Course newCourse = new Course(courseName, courseDes, cred);
        CourseDAO dao = new CourseDAO();
        
        boolean rowsAffected = dao.addCourse(newCourse);
        
        if (rowsAffected) {
                JOptionPane.showMessageDialog(this, "Course added successfully!");
                courseNameField.setText("");
                courseDesField.setText("");
                creditsField.setText("");
                refreshTable();
                gotoPnlTable();
            } else {
                JOptionPane.showMessageDialog(this, "Unable to add course!");
            }



        

    }//GEN-LAST:event_addCourseBtnActionPerformed

    private void delCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delCourseBtnActionPerformed

        String idStr = IdField.getText();
        int courseId;
                
        if(idStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Course ID to delete.");
            return;
        }

        try {
            
            courseId = Integer.parseInt(idStr);
            CourseDAO dao = new CourseDAO();
            
            boolean isDeleted = dao.deleteCourse(courseId);

            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Course deleted successfully!");
                IdField.setText("");
                refreshTable();
                gotoPnlTable();
            } else {
                JOptionPane.showMessageDialog(this, "Course ID not found!");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID must be a valid number!");
        }

    }//GEN-LAST:event_delCourseBtnActionPerformed

