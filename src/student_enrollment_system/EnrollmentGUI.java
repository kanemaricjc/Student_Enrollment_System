    private void centerTableText() {
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();

        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }
    
    void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        EnrollmentDAO dao = new EnrollmentDAO();
        ResultSet rs = dao.getAllEnrollments();
        
        if (rs != null) {
            try {
                while (rs.next()) {
                    
                    String studentName = rs.getString("first_name") + " " + rs.getString("last_name");
                    
                    Object[] row = {
                        rs.getInt("enrollment_id"),
                        studentName,
                        rs.getString("course_name"),
                        rs.getDate("enrollment_date")
                    };
                    model.addRow(row);
                }
            } catch (java.sql.SQLException e) {
                e.printStackTrace();
            }
        }
    }
    
    private void populateComboBoxes() {
        studentComboBox.removeAllItems();
        courseComboBox.removeAllItems();

        StudentDAO studentDAO = new StudentDAO();
        List<Student> studentList = studentDAO.getAllStudents();
        
        if (studentList != null) {
            for (Student s : studentList) {
                String fullName = s.getFname()+ " " + s.getLname();
                // Add the ComboItem object instead of a String
                studentComboBox.addItem(new ComboItem(s.getStudentid(), fullName));
            }
        }
        
        // Populate Courses
        CourseDAO courseDAO = new CourseDAO();
        List<Course> courseList = courseDAO.getAllCourses();
        
        if (courseList != null) {
            for (Course c : courseList) {
                // Add the ComboItem object instead of a String
                courseComboBox.addItem(new ComboItem(c.getCourseid(), c.getCoursename()));
            }
        }
    }
    
