    private void addEnrollmentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEnrollmentBtnActionPerformed
        
    if (studentComboBox.getSelectedItem() == null || courseComboBox.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select both a student and a course.");
            return;
        }

        try {
            // Retrieve the selected ComboItem objects
            ComboItem selectedStudent = (ComboItem) studentComboBox.getSelectedItem();
            ComboItem selectedCourse = (ComboItem) courseComboBox.getSelectedItem();

            // Extract the hidden IDs
            int studentId = selectedStudent.getId();
            int courseId = selectedCourse.getId();

            // Add the enrollment using your DAO
            EnrollmentDAO dao = new EnrollmentDAO();
            dao.addEnrollment(studentId, courseId);

            JOptionPane.showMessageDialog(this, "Student enrolled successfully!");
            
            // Refresh the table and return to view
            refreshTable();
            gotoPnlTable();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error processing enrollment.");
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_addEnrollmentBtnActionPerformed

    private void delEnrollmentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delEnrollmentBtnActionPerformed
    
    String idStr = jTextField1.getText();
        
        if(idStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Enrollment ID to delete.");
            return;
        }
        
        try {
            int enrollmentId = Integer.parseInt(idStr);
            EnrollmentDAO dao = new EnrollmentDAO();
            boolean isDeleted = dao.deleteEnrollment(enrollmentId);
            
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Enrollment deleted successfully!");
                jTextField1.setText("");
                refreshTable();
                gotoPnlTable(); 
            } else {
                JOptionPane.showMessageDialog(this, "Enrollment ID not found!");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID must be a valid number!");
        }    

    }//GEN-LAST:event_delEnrollmentBtnActionPerformed

